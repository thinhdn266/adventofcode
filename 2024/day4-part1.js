// keyword = 'XMAS'

keyword = {
    X: { next: 'M' },
    M: { prev: 'X', next: 'A' },
    A: { prev: 'M', next: 'S' },
    S: { prev: 'A' },
}

arr = data
    .trim()
    .split('\n')
    .map((line) => Array.from(line))

const cPos = (i, j) => {
    return `${arr[i][j]}[${i},${j}]`
}

const checkRow = (i, j, dir) => {
    if (j + 1 >= arr[i].length) return
    cur = arr[i][j]
    pos = [cPos(i, j)]
    while (arr[i][j + 1] === keyword[cur][dir]) {
        if (j + 1 >= arr[i].length) break
        j++
        cur = arr[i][j]
        pos.push(cPos(i, j))
    }
    if (pos.length === 4) return pos.join('_')
    return ''
}

const checkCol = (i, j, dir) => {
    if (i + 1 >= arr.length) return
    cur = arr[i][j]
    pos = [cPos(i, j)]
    while (arr[i + 1] && arr[i + 1][j] === keyword[cur][dir]) {
        if (i + 1 >= arr.length) break
        i++
        cur = arr[i][j]
        pos.push(cPos(i, j))
    }
    if (pos.length === 4) return pos.join('_')
    return ''
}

const checkDiag = (i, j, dir) => {
    if (i + 1 >= arr.length || j + 1 >= arr[i].length) return
    cur = arr[i][j]
    pos = [cPos(i, j)]
    while (arr[i + 1] && arr[i + 1][j + 1] === keyword[cur][dir]) {
        if (i + 1 >= arr.length || j + 1 >= arr[i].length) break
        i++
        j++
        cur = arr[i][j]
        pos.push(cPos(i, j))
    }
    if (pos.length === 4) return pos.join('_')
    return ''
}

const checkDiagNeg = (i, j, dir) => {
    if (i + 1 >= arr.length || j - 1 < 0) return
    cur = arr[i][j]
    pos = [cPos(i, j)]
    while (arr[i + 1] && arr[i + 1][j - 1] === keyword[cur][dir]) {
        if (i + 1 >= arr.length || j - 1 < 0) break
        i++
        j--
        cur = arr[i][j]
        pos.push(cPos(i, j))
    }
    if (pos.length === 4) return pos.join('_')
    return ''
}

res = []
for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr[i].length; j++) {
        res.push(checkRow(i, j, 'next'))
        res.push(checkRow(i, j, 'prev'))
        res.push(checkCol(i, j, 'next'))
        res.push(checkCol(i, j, 'prev'))
        res.push(checkDiag(i, j, 'next'))
        res.push(checkDiag(i, j, 'prev'))
        res.push(checkDiagNeg(i, j, 'next'))
        res.push(checkDiagNeg(i, j, 'prev'))
    }
}

